// Helper functions for events data
export const getEventsByBarId = (barId: number): EventData[] => {
  return allEvents.filter(event => event.barId === barId && event.status === 'upcoming');
};

export const getUpcomingEvents = (): EventData[] => {
  const now = new Date();
  return allEvents.filter(event => {
    const eventDate = new Date(event.date);
    return eventDate >= now && event.status === 'upcoming';
  }).sort((a, b) => new Date(a.date).getTime() - new Date(b.date).getTime());
};

export const getEventById = (id: number): EventData | undefined => {
  return allEvents.find(event => event.id === id);
};

export const getEventsByCategory = (category: EventData['category']): EventData[] => {
  return allEvents.filter(event => event.category === category && event.status === 'upcoming');
};

export const getEventsByLocation = (location: string): EventData[] => {
  return allEvents.filter(event => 
    event.barLocation.toLowerCase() === location.toLowerCase() && 
    event.status === 'upcoming'
  );
};