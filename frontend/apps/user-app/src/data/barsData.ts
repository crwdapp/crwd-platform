// Helper functions
export const getBarsByLocation = (location: string): Bar[] => {
  return allBars.filter(bar => bar.location === location.toUpperCase());
};

export const getBarById = (id: number): Bar | undefined => {
  return allBars.find(bar => bar.id === id);
};

export const getBarsByFilter = (filters: string[]): Bar[] => {
  return allBars.filter(bar => 
    filters.some(filter => bar.filters.includes(filter))
  );
};

export const getOpenBars = (): Bar[] => {
  return allBars.filter(bar => bar.isOpen);
};

export const getBarsByRating = (minRating: number): Bar[] => {
  return allBars.filter(bar => bar.rating >= minRating);
};

